<p>之所以会写这篇文章，考虑到两点：一点是总结自己的面试经历与经验，以便为了以后更好的应对面试；另外一点是希望能够给那些即将准备面试的大学生(以及其他学生)点帮助，望在接下来的面试中能够提供点参考意见和帮助。</p>

<p>有几点需要说明一下：（一）我不会教你该怎么写简历，其实网上已经有很多的资源了；（二）不要期望看了这篇文章就觉得自己能够进入BAT了，我自己目前的面试水平还没达到那个水平，所以我也不知道该怎么教你。</p>



<h2 id="累"><a name="t1"></a>累</h2>

<p>如果你要问我那段面试时间给你最大感受是什么，我会说：累，是真的累。我在投简历的时候把范围限制在了江浙沪这一带，其实也就上海，南京，杭州这几个城市。所以需要我不停的奔波。我记得我是先把南京（当时我在南京）的面试完了，大概有三四家吧，然后又去杭州三四天，总共面了两家，推了一家，然后去上海，不过上海就一天，也就是面我现在所在的公司。我记得那天是早上五点多从南京坐火车去上海，然后迟了一个小时（打电话跟公司说了，可以延迟），面试完，当天下午就又回南京了，其实当时我还想在上海面试两家，但那个时候就觉得累，真的，所以就决定回去了，不面了（因为那个时候我已经得到口头Offer了）。其实还有一个原因是我之前去杭州面试已经拿到了一家Offer，所以就….你懂的。</p>

<p>我记得那天早上五点多起床坐火车去上海的路上，我发现整个火车上都是人，你会发现这个世界上原来有那么多人跟你一样在为了生活奔波，虽然大家的目的不尽相同，但都是为了有个更好的生活。之前工作都是八点半去公司，你会发现过斑马线的时候会有很多跟你一样的人，其实哪怕五六点，也会有很多人也已经工作了。</p>

<p>但是我觉得，这个累从某种程度上是可以减轻的。以后大家在面试的时候，尽量把时间安排得宽松点，其实我这个还好，一天只有一个面试，我之前看过别人有一天两个面试的。如果面了其他城市的，首先考虑可不可以电面或者视频面试等等，如果不行，再考虑自己有没有那个必要为了这个面试去奔波。</p>



<h2 id="套路"><a name="t2"></a>套路</h2>

<p>最大的收获呢？套路…哈哈，可能有的人会觉得很奇怪。此套路非彼套路。面试作为工作，对的，对于HR来说面试就是工作的一部分。既然是工作，那一定是有规则（准则），其实可以理解为套路啦。也就是说你得按照规则来。面试有什么规则呢？认真对待简历，做好笔试，回答好问题….</p>

<p>当然，套路是相对于绝大多数的人来说的，如果你觉得自己不是那绝大多数，那你可以突破常规，来个不一样的也是可以的。不知道你之前有没有看个一个很吊的一个面试，大概是前端吧，就做了一个网页，这种创意是很好的。哦，对了，忘记跟你说了，作者应该在国外，哈哈..</p>

<p>所以要想拿到Offer，你就需要学会从HR的角度考虑问题，想想如果你是HR你看到什么的简历你会让它过；再想想如果你是面试官，你希望面试到什么的人，什么样的人你会觉得他的技术可以，符合公司的要求；再想想如果你是人事主管（CTO或者CEO）你需要对方具有什么样的性格…</p>

<p>这个东西我觉得是非常的重要，原因在于：绝大多数的情况下，看你简历的人之前是不了解你的（你是著名人物除外）所以他想了解你只能通过你的简历，而筛选简历作为工作的一部分，那肯定是有标准的（有的公司可能是HR筛选一下，然后给技术总监在筛一下）。这个时候你就要学会换位思考了，这个标准是什么呢？你在结合一下你自己的面试岗位，我想你应该知道的。</p>

<p>其实之所以会有这种想法，由来已久。我记得我大三时候考英语六级的时候，我是在考试10天前开始准备的。大一考过四级以后我应该就没有认真看过英语课本了。所以我并没有非常好的英语底子。还有十天考试，如果是你你会怎么做？背单词?做真题?参加培训?买押题?我记得当时我想的就是：首先我肯定得不了高分，一个有自知之明的人应该都知道的；其次我肯定要及格（其实英语六级没有及格说法，但很多人都会觉得425就是及格分，在这里我不去争辩），如果不及格那不就是只参与了嘛。还有一点需要提的是：及格分从来不是用来为难人的，也就是说对于绝大多数人只要你认真准备，就算考的再差也不会不及格的。在这种指导思想下，我就开始了我的复写。其实也没啥，我就做了三四份真题，然后就一直看真题分析，并没有刻意的被单词，也没有被什么预测题。其实我这样做的目的就是希望能够了解试卷的套路，然后在做试卷的时候我就尽量按照套路来。最终也是考过了，我想说的是其实我并不厉害，我所做的只是尽可能的多去了解对方。知己知彼，才能那个啥嘛。所以举这个例子我只是想说明：套路或者说是规则对于一个行业来讲是极其的重要的。</p>

<p>所以以后准备面试的时候多去了解你所投递的公司，多去了解公司的招聘要求，多了解自己。</p>



<h2 id="关于简历"><a name="t3"></a>关于简历</h2>

<p>简历怎么写我不会教你，因为我也是跟别人学的，有几个点需要注意下：</p>

<ol>
<li>保持简历的实时性，尽量保持你的简历时效性，很多时候当你决定要换工作的时候在写简历了，那个时候你就会遇到人生的一大难题：妈的，我过去几年做了啥？所以在你工作中如果学到了新的技能或者新的语言的时候就可以及时的添加到你的简历中了。插一句，当你工作了要学会写工作报告，不是为了给老板看啊，是给自己总结的，以后写简历或者总结自己工作的时候也会有个参考资料，我目前已经在坚持了，我是按月来写的，写的内容无外乎就是这个月我做了啥，学到了啥。很简单的。</li>
<li>真实性。一方面指的是不要作假，不要有侥幸心里，没用的；另一方面指的是和自己目前的水平相匹配，也就说简历上写的就是你的真实能力。不要觉得性能优化，这种词多么牛B啊，写在简历上肯定牛逼哄哄。额…性能优化不是一个实习生要做的事（不是说你不会，而是在性能优化上就算你工作了也不一定要你做）。</li>
<li>具体。写简历尽量突出你的工作内容（有实习经历），而你的工作内容肯定不是整天性能优化，代码优化吧。把你具体做的东西写出来，然后在深化到理论知识点。</li>
</ol>



<h2 id="关于面试"><a name="t4"></a>关于面试</h2>

<p>其实这个没啥要写的，很多时候都取决于你的面试官，如果面试官人好，可能不要几分钟就过了。不要怀疑，我遇到过两次了：在杭州面试的时候，技术面试官来面试我的时候，总共花儿三四分钟，然后就直接找负责人来面了。上海这边，就看了一下我做的题，然后看了我的简历，就过了。对于实习生，很多东西要求并不会那么高，当然我也遇到过问的很深的，那个时候我就觉得：我只是来面试个实习生，要这样吗！这个时候也没办法，所以在准备面试的时候要好好准备，哈哈。</p>

<p>准备技术面试官的面试其实还好，毕竟大家的工作的一样，所以在准备的时候把基础知识，简历上的实习经历准备好就OK了。但是在回答HR，负责人的问题的时候就需要提前准备了，其实网上已经有人总结好了。我在面试的过程中被问的最多的问题有：为什么离开上个公司？为什么要来上海?你了解我们公司吗？你有什么要问我的？其实都是套路，只要你面试你几乎逃不了，尤其是最后一个，哈哈。所以这个需要大家提前准备好，自己也练练。</p>



<h2 id="关于其他"><a name="t5"></a>关于其他</h2>

<p>找工作是件大事，这个毫无疑问。所以既然是大事，那你一定要准备充分，同时很多细节也要注意。</p>

<p>在和HR聊天的时候要体现出对对方的尊重，这个不仅仅是在面试，其实在很多时候，如果你有求于人，那一定要表示出你对对方的尊重以及对这个件事的重视。绝大多数的HR都是非常友好的，当然长的也是很漂亮的，反正我遇到的都是：）。所以只要你按照套路来基本上拿到面试资格一点问题都没有。怎么尊重？无外乎就是敬语（你好，谢谢，祝您工作顺利生活愉快等等），还是那句话，多从对方的角度考虑问题。</p>

<p>面试的时候不要说出很随意的话，陌生人在聊天的时候大家都是很警觉的，只要你的肢体语言或者言语透露出你的不想要拿到Offer那你就JJ了。一定要稳，带着诚意而来，拿着Offer而走！</p>

<p>很多时候你要相信自己不是自己不行，而是没有遇到你合适的。如果一家不行就在投另外一家嘛，找不到工作是懦夫的表现。</p>



<h2 id="其他小事"><a name="t6"></a>其他小事</h2>

<p>HR绝对是一个颜值很高的职业，要么不可能HR都那么漂亮，简直<a href="https://www.baidu.com/s?wd=%E4%B8%8D%E5%8F%AF%E6%80%9D%E8%AE%AE&amp;tn=24004469_oem_dg&amp;rsv_dl=gh_pl_sl_csd" target="_blank">不可思议</a>。</p>

<p>我刚进入这个行业的时候（最开始实习）我觉得无论是初创公司还是已经做到行业领先的公司觉得没啥区别，但现在过来，我只想说，不是没啥区别，简直就不能放在一起比。如果一个公司已经做到自己所在行业<a href="https://www.baidu.com/s?wd=%E6%95%B0%E4%B8%80%E6%95%B0%E4%BA%8C&amp;tn=24004469_oem_dg&amp;rsv_dl=gh_pl_sl_csd" target="_blank">数一数二</a>的了，那么<a href="https://www.baidu.com/s?wd=%E6%97%A0%E8%AE%BA%E5%A6%82%E4%BD%95&amp;tn=24004469_oem_dg&amp;rsv_dl=gh_pl_sl_csd" target="_blank">无论如何</a>，这个公司基本上是靠谱的，那也意味着公司在招人上也是比较靠谱的，这个时候只要你觉得自己是个靠谱人那投出的简历基本上会收到面试机会的。但是对于初创或者相对来说规模较小的公司来说，这就是看你的运气了。运气好的话你会遇到一个牛逼的创始人（在中国很多SB都觉得自己很牛B，甚至觉得自己就TM是乔布斯的接班人），我在电话面试的时候有幸和一个很牛逼的联合创始人聊过，从他问的问题你就可以感觉到技术上是真的牛逼。但如果遇到一些…那些…哈哈，今早远离吧。反正我这辈子是不会在考虑初创了，除去一种可能就是是我的朋友或者我认识的人开始创业，我可能会去帮忙的（如果他需要的话：））。肯定有人会说你怎么能这样想呢？对不起，我现在只能这么想，因为我有经历了一些事情，而且我也不愿意再次经历了。</p>

<p>我喜欢做技术的人，不喜欢做技术的负责人。</p>

<p>还有件事我想跟大家分享一下。</p>

<p>去面试杭州那家公司的时候，已经面过了HR，技术，负责人，最后好像还有一个总经理（记得不太清）要面吧，当时HR跟我说总经理不在让我先回去，说回来了在打电话给我，说可能也不需要了，就直接发Offer了，但是当我到车站的时候（回南京）HR打电话过来了说总经理想见见我，当时没办法，为了工作，所以就又坐车回去（一个小时）。和总经理面试的时候，可能是年长的原因（像极了大公司的董事长）问了我很多…人品相关的问题，我记得刚开始的几分钟还是很紧张的，说的话都是抖的。问了很长时间，应该不低于半个小时，问的问题很多，我记的有：你有女朋友吗？什么时候谈的？为什么分手了？你觉得江苏怎么样？你觉得杭州怎么样？反正就是考验人品的问题。给我的感觉就是像是党在考验我。其实之所以觉得很不一样，是没想到，我只是一个实习生而已，而且之前技术负责人也已经问过了。所以按照道理说应该没了。还有一个原因（我猜的）公司应该是属于政府企业，具体的我也不太好描述，就是公司应该和政府有关系，不过公司是真的有钱，大半个楼层都是它的，但里面的空座位是真的多，就感觉不缺钱。事实证明的确不缺钱。</p>

<p>还有件事，就是面试过接到HR的电话说Offer已发的时候，那感觉是TM真的爽。</p>



